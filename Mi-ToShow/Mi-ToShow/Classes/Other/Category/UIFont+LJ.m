#import "UIFont+LJ.h"

@implementation UIFont (LJ)

/*
 * 用户账户中心的TextFile字体
 */
+ (UIFont *)LJUserManagerTextFileFont {
    return [UIFont systemFontOfSize:17];
}

+ (UIFont *)LJUserManagerTextFilePlaceHolderFont {
    return [UIFont systemFontOfSize:14];
}

+ (UIFont *)LJUserManagerLeftLabelFont {
    return [UIFont systemFontOfSize:15];
}


@end
